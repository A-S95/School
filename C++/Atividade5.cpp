#include <iostream>

int main() {
    int numUm, numDois;
    
    std::cout << "Insira dois valores: " << std::endl;
    std::cin >> numUm >> numDois;
    
    std::cout << "O Resultado do resto da divisao e: " << numUm % numDois << std::endl;
    std::cout << "O Resultado da soma e: " << numUm + numDois << std::endl;
    std::cout << "O Resultado da multiplicacao e: " << numUm * numDois << std::endl;
    std::cout << "O Resultado da subtracao e: " << numUm - numDois << std::endl;
    std::cout << "O Resultado da divisao e: " << numUm / numDois << std::endl;
    
}
